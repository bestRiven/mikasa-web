package com.mikasa.vo;

public class RedPackageTemp {

	private int id;
	private String template_des;
	private String template_url;
	private String img_url;
	private String advertisement_url;
	private String coupon_code;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getTemplate_des() {
		return template_des;
	}
	public void setTemplate_des(String template_des) {
		this.template_des = template_des;
	}
	public String getTemplate_url() {
		return template_url;
	}
	public void setTemplate_url(String template_url) {
		this.template_url = template_url;
	}
	public String getImg_url() {
		return img_url;
	}
	public void setImg_url(String img_url) {
		this.img_url = img_url;
	}
	public String getAdvertisement_url() {
		return advertisement_url;
	}
	public void setAdvertisement_url(String advertisement_url) {
		this.advertisement_url = advertisement_url;
	}
	public String getCoupon_code() {
		return coupon_code;
	}
	public void setCoupon_code(String coupon_code) {
		this.coupon_code = coupon_code;
	}
	
	
}
